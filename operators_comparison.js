let x = 5;

// Equal to
let ansA = x == 8; // false
let ansB = x == 5; // true
let ansC = x == "5"; // true

// Equal value and Equal type
let ansD = x === 5; // true
let ansE = x === "5"; // false

// Not Equal

let ansF = x != 8; // true
let ansG = x !== 5; // false
let ansH = x !== 8; // true

// Greater than
let ansI = x > 8; // false

// Less than
let ansJ = x < 8; // true

// Greater than or equal to
let ansK = x >= 8; // false
let ansL = x <= 8; // true

